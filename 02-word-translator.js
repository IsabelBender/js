let language = window.prompt('Please enter the code for your preferred language: es, de, en or fr'); 
if (language === es) {
  window.console.log('Hola el mundo'); 
} else if (language === de) {
  window.console.log('Hallo welt'); 
} else if (language === en) {
  window.console.log('Hello world'); 
} else if (language === fr) {
  window.console.log('Bonjour le monde'); 
}

